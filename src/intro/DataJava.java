package intro;

public class DataJava {
    public static void main(String[] args){

        String name="Praveena";
        byte age= 25;
        short salary= 32767;
        int annual_sal=3245678;
        long phone_num=9876546778l;  // l denotes long

        float cgpa= 8.3f; //f denotes float
        double cgpa2=7.3d; //d denotes double

        System.out.println("My name is" +name);
        System.out.println("My age is" +age);
        System.out.println("My salary is" +salary);
        System.out.println("My annualsalary is" +annual_sal);
        System.out.println("My phone number is" +phone_num);
        System.out.println("My cgpa is" +cgpa);
        System.out.println("My cgpa2 is" +cgpa2 );

    }
}
